export function getAssessmentActivities(assessmentId: string): AssessmentActivity[] {
  return baseActivities.map((activity) => {
    const act = {
      ...activity,
      assessmentId,
      id: activity.id.replace('ASS_001', assessmentId),
    };

    // If there is no base rating, it remains unrated
    if (act.rating === 0) {
      act.l1Rating = 0;
      act.l2Rating = undefined;
      act.l3Rating = undefined;
      act.l4Rating = undefined;
      return act;
    }

    if (assessmentId === 'ASS_008') {
      // Draft (L1 is editing)
      act.l1Rating = act.rating;
      act.l2Rating = undefined;
      act.l3Rating = undefined;
      act.l4Rating = undefined;
    } else if (assessmentId === 'ASS_002') {
      // Pending L3 (AVP) - L1 and L2 scores are present
      act.l1Rating = Math.max(1, Math.round((act.rating - 0.4) * 10) / 10);
      act.l2Rating = act.rating;
      act.l3Rating = undefined;
      act.l4Rating = undefined;
    } else if (assessmentId === 'ASS_003') {
      // Pending L4 (BH) - L1, L2, L3 scores are present
      act.l1Rating = Math.max(1, Math.round((act.rating - 0.5) * 10) / 10);
      act.l2Rating = Math.max(1, Math.round((act.rating - 0.2) * 10) / 10);
      act.l3Rating = act.rating;
      act.l4Rating = undefined;
    } else {
      // Approved or other - all scores present
      act.l1Rating = Math.max(1, Math.round((act.rating - 0.4) * 10) / 10);
      act.l2Rating = Math.max(1, Math.round((act.rating - 0.2) * 10) / 10);
      act.l3Rating = act.rating;
      act.l4Rating = act.rating;
    }

    return act;
  });
}

